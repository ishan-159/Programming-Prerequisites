class AveragePercentMarks{
public static void main(String[] args){

//Create maths variable to store maths marks
int maths = 94;

//Create physics variable to store physics marks
int physics = 95;

//Create chemistry variable to store chemistry marks
int chemistry = 96;

//Calculate average percent mark of sam 
int average = (maths + physics + chemistry) / 3;

//Printing the result
System.out.println("Sam's average mark in PCM is " + average);
}
}